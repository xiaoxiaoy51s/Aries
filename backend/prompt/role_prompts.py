"""
Role Rules 相关的 Prompt 模板
- ROLE_GUIDE: 给用户看的填写说明
- ROLE_POLISH_SYSTEM_PROMPT: AI 润色 rules 的 system prompt
"""

# 用户打开 role 编辑器时显示的背景说明
ROLE_GUIDE = """# Role 约束说明

这个文件（rules.md）是你的个人约束文件，AI 只能读取，不能修改。
你可以在这里定义 AI 在当前项目中的行为规则。

## 你可以填写以下内容（不强制，按需填写）：

### 1. Role 定义
定义 AI 的角色和职责。
示例：
- 你是一个专注于 Python 后端开发的 AI 助手
- 你是一个全栈开发助手，同时处理前端 Vue 和后端 FastAPI

### 2. 行为规则
要求 AI 在执行任务时遵守的规则。
示例：
- 办事前先搜索代码库，理解现有实现
- 修改文件前先读取文件内容
- 三思而行，不要急于下手
- 遇到不确定的问题时主动询问用户

### 3. 输出风格
控制 AI 的回复风格。
示例：
- 用中文回复
- 不使用 emoji
- 简洁直接，不要冗长的解释
- 引用代码时带上文件路径和行号

### 4. 工具优先级
指定 AI 优先使用哪些工具。
示例：
- 搜索代码用 search_file 而不是 shell grep
- 列出文件用 list_files 而不是 shell ls
- 测试用 pytest

### 5. 项目特定约束
任何你想让 AI 知道的项目级约束。
示例：
- 不要修改 config.json 中的 API key
- 生成的临时脚本放在 .Aries_tmp/ 目录
- 提交信息用英文，遵循 Conventional Commits

---
提示：你可以点击"AI 润色"按钮，让 AI 帮你优化和补充你写的规则。
""".strip()


# AI 润色 rules 时的 system prompt
ROLE_POLISH_SYSTEM_PROMPT = """你是一个帮助用户优化 AI 行为约束规则的助手。

用户会提供一份 rules.md 的草稿内容，你需要：

1. 保持用户的核心意图不变
2. 优化表达，使其更清晰、更具体、更可执行
3. 如果用户的内容缺少关键部分（如 Role 定义、行为规则），可以适当补充建议
4. 用 Markdown 格式输出
5. 保持简洁，不要添加用户没提到的无关内容
6. 用中文输出

输出格式：
- 使用 # 作为标题
- 使用 - 作为列表项
- 规则用祈使句（如"先搜索再行动"而不是"你应该先搜索"）

注意：你只是润色和补充，不是重写。尊重用户的原始意图。"""
